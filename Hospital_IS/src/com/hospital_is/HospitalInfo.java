    private void searchByNameButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchByNameButtonMouseClicked
        String hospitalName = searchByHospitalNameField.getText().trim();
        SelectionSort.nameBasedSelectionSort(tablePage.getHospitalDatas()); 
        addSortedData();
        if (hospitalName.isEmpty()) { 
            new MessageDialogBox(this, "Please enter hospital name", true).setVisible(true);
        }else {
            int index = BinarySearch.hospitalNameBinarySearch(tablePage.getHospitalDatas(), hospitalName);
            if (index == -1) {
                    new MessageDialogBox(this, "No any hospital data found", true).setVisible(true);
            }else {
                String message = getHospitalSearchInformationAtIndex(index); 
                JOptionPane.showMessageDialog(this, message); 
            }
        }
    }//GEN-LAST:event_searchByNameButtonMouseClicked

    private void searchByPriceButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchByPriceButtonMouseClicked
        String priceInString = searchByPriceField.getText().trim();
        SelectionSort.priceBasedSelectionSort(tablePage.getHospitalDatas());
        addSortedData();
        try {
            double price = Double.parseDouble(priceInString);
            int index = BinarySearch.admittedPriceBinarySearch(tablePage.getHospitalDatas(), price);
            if (index == -1) {
                new MessageDialogBox(this, "No any hospital data found", true).setVisible(true);
            }else {
                String message = getHospitalSearchInformationAtIndex(index);
                JOptionPane.showMessageDialog(this, message);
            }
        }catch(NumberFormatException numberFormatException) {
            new MessageDialogBox(this, "Please enter price in numeric form", true).setVisible(true); 
        }
    }//GEN-LAST:event_searchByPriceButtonMouseClicked

    private void searchByCategoryButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchByCategoryButtonMouseClicked
        String hospitalCategory = "";
        if (searchByGovernment.isSelected())
            hospitalCategory = "Government";
        else if (searchByPrivate.isSelected())
            hospitalCategory = "Private"; 
        
        if (hospitalCategory.isEmpty()) {
            new MessageDialogBox(this, "Please select at least one category", true).setVisible(true);  
        }else {
            SelectionSort.categoryBasedSelectionSort(tablePage.getHospitalDatas()); 
            addSortedData();
            boolean found = false;
            String message = "The hospital names with " + hospitalCategory + " category:-\n";
            for(int i = 0; i < tablePage.getHospitalDatas().size(); i++) {
                if(tablePage.getHospitalDatas().get(i).getHospitalCategory().equals(hospitalCategory)) {
                    found = true;
                    message += tablePage.getHospitalDatas().get(i).getHospitalName() + "\n";
                } 
            }
            if(!found) 
                new MessageDialogBox(this, "No any hospital data found", true).setVisible(true);
            else {
                JOptionPane.showMessageDialog(this, message);
            }
        }
    }//GEN-LAST:event_searchByCategoryButtonMouseClicked

