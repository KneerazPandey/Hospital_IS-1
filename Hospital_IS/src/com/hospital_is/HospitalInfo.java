    public void enableTypingNumberKeyOnly(KeyEvent event) {
        char userTypedCharacter = event.getKeyChar();
        if(!(Character.isDigit(userTypedCharacter)) || (userTypedCharacter == KeyEvent.VK_BACK_SPACE)
                || (userTypedCharacter == KeyEvent.VK_DELETE)) {
            event.consume();
        }
    }
    
    public void addSortedData() {
        DefaultTableModel tableModel = (DefaultTableModel) tablePage.getHospitalInfoTable().getModel();
        tableModel.setRowCount(0); 
        for(HospitalData data : tablePage.getHospitalDatas()) {
            String values[] = {data.getHospitalName(), data.getAddress(), String.valueOf(data.getTelephoneNumber()), 
                data.getHospitalCategory(), String.valueOf(data.getAdmittedPrice()), data.getHealthInsurence()};
            tableModel.addRow(values); 
        }
    }
    
    public String getHospitalSearchInformationAtIndex(int index) {
        String name = tablePage.getHospitalDatas().get(index).getHospitalName();
        String address = tablePage.getHospitalDatas().get(index).getAddress();
        long telephone = tablePage.getHospitalDatas().get(index).getTelephoneNumber();
        String category = tablePage.getHospitalDatas().get(index).getHospitalCategory();
        double price = tablePage.getHospitalDatas().get(index).getAdmittedPrice();
        String insurance = tablePage.getHospitalDatas().get(index).getHealthInsurence();
        String message = "--------------------Hospital Information--------------------\n"
                + "Name: " + name +"\n"
                + "Address: " + address +"\n"
                + "Telephone: " + telephone +"\n"
                + "category: " + category +"\n"
                + "Price: " + price +"\n" 
                + "Insurance: " + insurance +"\n"; 
        return message;
    }

